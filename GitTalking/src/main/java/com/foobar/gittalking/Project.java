package com.foobar.gittalking;

public class Project {
	public int ID;
	public String name;
	public String status;
	
	public int getID() {
		return ID;
	}
	
	public String getName() {
		return name;
	}
	
	public String getStatus() {
		return status;
	}
	
	public void setID(int id) {
		ID = id;
	}
	
	public void setName(String n) {
		name = n;
	}
	
	public void setStatus(String s) {
		status = s;
	}
}
